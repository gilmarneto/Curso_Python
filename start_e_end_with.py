# declarando variável
nome = "Gilmar Florencio Neto"

# comparação_startwith
print(nome.startswith("G")) # verifica se a primeira letra é G maiúscula = True
print(nome.startswith("g")) # verifica se a primeira letra é g minúscula = False
# lembrando que python é case sensitive, diferencia maiúsculas e misnúsculas

# comparação_endswith
print(nome.endswith("o")) # verifica se a última letra da String é 'o' mínúsculo
print(nome.endswith("O")) # verifica se a última letra da String é 'O' maiúsculo


